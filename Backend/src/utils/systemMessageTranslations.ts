function flattenEntityOverrides(): Record<string, Record<string, string>> {
  const result: Record<string, Record<string, string>> = {};

  for (const [lang, entities] of Object.entries(ENTITY_OVERRIDES)) {
    result[lang] = { ...(result[lang] ?? {}) };
    for (const entityType of ENTITY_TYPES) {
      const messages = entities[entityType];
      for (const messageType of ENTITY_MESSAGE_TYPES) {
        result[lang][`chat.systemMessages.${entityType}.${messageType}`] = messages[messageType];
      }
    }
  }

  return result;
}

export const systemMessageTranslations: Record<string, Record<string, string>> = (() => {
  const flattened = flattenEntityOverrides();
  const merged: Record<string, Record<string, string>> = {};

  for (const lang of Object.keys(BASE_SYSTEM_MESSAGES)) {
    merged[lang] = {
      ...BASE_SYSTEM_MESSAGES[lang],
      ...(flattened[lang] ?? {}),
    };
  }

  return merged;
})();
